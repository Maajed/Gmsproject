    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        String id = jLabel3.getText();
        String name=jTextField2.getText();
        String email=jTextField3.getText();
        String gender =(String)jComboBox1.getSelectedItem();
        String NextofKin = jTextField4.getText();
        String gymtime =(String)jComboBox2.getSelectedItem();
        String PhoneNumber = jTextField1.getText();
        String age = jTextField5.getText();
        String amount = jTextField6.getText();
      
        
        try
        {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps= con.prepareStatement("insert into member values(?,?,?,?,?,?,?,?,?");
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, email);
            ps.setString(4, gender);
            ps.setString(5, NextofKin);
            ps.setString(6, gymtime);
            ps.setString(7, PhoneNumber);
            ps.setString(8, age);
            ps.setString(9, amount);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully saved");
            setVisible(false);
            new NewMember().setVisible(true);
             
                    
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
            
        }
        
    }//GEN-LAST:event_SaveButtonActionPerformed

